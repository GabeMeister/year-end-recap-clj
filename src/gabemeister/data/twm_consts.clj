(defn- build-class-map
  [class-group-str classes-vec]
  (loop [class-map {}
         classes-vec classes-vec]
    (if (empty? classes-vec)
      class-map
      (let [item (first classes-vec)]
        (recur (assoc class-map item class-group-str)
               (rest classes-vec))))))

(defn- class-group-reducer
  [acc [class-group-str classes-vec]]
  (let [class-map (build-class-map class-group-str classes-vec)]
    (merge acc class-map)))

(defn- get-class-map
  [class-groups]
  (let [class-map (reduce class-group-reducer {} class-groups)]
    class-map))

(def get-class-map-memoized (memoize get-class-map))

(def CLASS-MAP (get-class-map-memoized CLASS-GROUPS))

